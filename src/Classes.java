public class Classes {
    public int classID;
    public String classType;
    public int classLevel;
    public int classSection;
    public int classStartTime;
    public int classEndTime;
    public String classTeacher;

    public int getClassID() {
        return classID;
    }

    public void setClassID(int classID) {
        this.classID = classID;
    }

    public String getClassType() {
        return classType;
    }

    public void setClassType(String classType) {
        this.classType = classType;
    }

    public int getClassLevel() {
        return classLevel;
    }

    public void setClassLevel(int classLevel) {
        this.classLevel = classLevel;
    }

    public int getClassSection() {
        return classSection;
    }

    public void setClassSection(int classSection) {
        this.classSection = classSection;
    }

    public int getClassStartTime() {
        return classStartTime;
    }

    public void setClassStartTime(int classStartTime) {
        this.classStartTime = classStartTime;
    }

    public int getClassEndTime() {
        return classEndTime;
    }

    public void setClassEndTime(int classEndTime) {
        this.classEndTime = classEndTime;
    }

    public String getClassTeacher() {
        return classTeacher;
    }

    public void setClassTeacher(String classTeacher) {
        this.classTeacher = classTeacher;
    }
    
    
            
}
